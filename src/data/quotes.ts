export function getQuoteForMoodAndSeed(mood: MoodKey, seed: string): string {
  const quotes = quotesDB[mood];
  let hash = 0;
  for (let i = 0; i < seed.length; i++) {
    const char = seed.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash |= 0;
  }
  const index = Math.abs(hash) % quotes.length;
  return quotes[index];
}

export function generateSeed(mood: MoodKey, name: string, date: string, extra: number = 0): string {
  return `${mood}-${name}-${date}-${extra}`;
}
